package week01.anhyeon;

import java.util.Scanner;

public class Solution_BOJ_2741 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);

        int N = sc.nextInt();

        for (int i = 0; i < N; i++) {
            System.out.println(i + 1);
        }

        sc.close();
    }
}
